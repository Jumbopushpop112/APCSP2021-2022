def main():
    bob = turtle.Turtle()
    wn = turtle.Screen()
    file = open("dogBreeds.csv", "r")
    csv_reader = csv.reader(file)
    lineList = []
    for row in csv_reader:
        lineList.append(row)
    writePoodleAttributes(lineList[0], bob, wn)
    writeRetrieverAttributes(lineList[1], bob, wn)
    writeHuskyAttributes(lineList[2], bob, wn)
    writeChihuahuaAttributes(lineList[3], bob, wn)
    writeBulldogAttributes(lineList[4], bob, wn)
    wn.exitonclick()  # last statement
main()
