import turtle
import csv
def repositionTurtle(x, y, myTurtle):
       myTurtle.goto(x,y)
def writePoodleAttributes(linePoodle, myTurtle, window):
       myTurtle.penup()
       repositionTurtle(-420, 100, myTurtle)
       window.addshape("Poodle.gif")
       myTurtle.shape("Poodle.gif")
       myTurtle.stamp()
       myTurtle.shape('classic')
       myTurtle.right(90)
       myTurtle.forward(150)
       myTurtle.write(linePoodle[0], align="center")
       myTurtle.forward(25)
       myTurtle.write(linePoodle[1], align="center")
       myTurtle.forward(25)
       myTurtle.write(linePoodle[2], align="center")
       myTurtle.forward(25)
       myTurtle.write(linePoodle[3], align="center")
       repositionTurtle(-250, 100, myTurtle)
