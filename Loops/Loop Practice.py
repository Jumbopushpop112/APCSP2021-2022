#loop through a string and print each character
swim = "swim" 
for element in swim: 
    print(element)    

#loop through an integer and print all the previous values
x = 6
for y in range(x): 
    print(y)

#while loop
i = 1
while i < 6:
    print(i)
    i += 1

#while loop with break statement
i = 1
while i < 6:
    print(i)
    if(i == 3): 
        break
    i += 1
